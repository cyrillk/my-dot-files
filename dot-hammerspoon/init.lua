-- Window moving

local MOVE_SIZE = 50

hs.hotkey.bind({"cmd", "alt", "ctrl"}, "Y", function()
  local win = hs.window.focusedWindow()
  local f = win:frame()

  f.x = f.x - MOVE_SIZE
  f.y = f.y - MOVE_SIZE
  win:setFrame(f)
end)

